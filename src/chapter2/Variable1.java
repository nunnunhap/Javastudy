package chapter2;

// 변수 : 기억장소를 가리키는 이름
/*
 * 자바스크립트 : 변수를 선언하는 키워드 let, var, const
 */
public class Variable1 {

	public static void main(String[] args) {
		//변수를 선언
		//데이터타입 : byte, short, int, long
		//int 는 기억장소를 만드는 것이고 level은 기억장소의 이름을 만드는 것.
		int level;
		level = 10;
		
		System.out.println(level); //메소드!
	}

}
